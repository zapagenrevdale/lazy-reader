export default $config({
  app(input) {
    return {
      name: "levels-learning",
      removal: input?.stage === "production" ? "retain" : "remove",
      protect: ["production"].includes(input?.stage),
      home: "aws",
    };
  },
  async run() {
    const { api, bucket } = await import("./src/resources")

    return {
      Api: api.url,
      Bucket: bucket.name,
    }
  },
});
